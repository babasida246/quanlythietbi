// Verify parity
function countLeaves(obj, prefix = '') {
    let count = 0;
    for (const [k, v] of Object.entries(obj)) {
        if (typeof v === 'object' && v !== null) count += countLeaves(v, prefix + k + '.');
        else count++;
    }
    return count;
}

